# 5 Defference Between Compiler and Interpreter
''' 
1. Compilation vs Interpretation:
   - Compiler: Translates the entire source code into machine code before execution.
   - Interpreter: Translates and executes the source code line by line.
2. Execution Speed:
   - Compiler: Generally faster execution since the entire code is compiled beforehand.
   - Interpreter: Slower execution due to line-by-line translation during runtime.
3. Error Detection:
   - Compiler: Detects errors during the compilation phase before execution.
   - Interpreter: Detects errors during runtime, which may lead to partial execution before an error occurs.
4. Output Generation:
   - Compiler: Produces an independent executable file that can be run without the source code. 
    - Interpreter: Does not produce an independent executable; the source code is required for execution.
5. Use Cases:
   - Compiler: Commonly used for languages like C, C++, and Java.
   - Interpreter: Commonly used for languages like Python, JavaScript, and Ruby.
'''

