# Keywords in Python
# What are Keywords?
# Keywords are reserved words in Python that have special meaning and cannot be used as variable names, function names, or any other identifiers.
# They are used to define the syntax and structure of the Python language.  


# Reserved Keywords
# Python has a set of reserved keywords that cannot be used as variable names. Some of the
# reserved keywords in Python are: and, as, assert, break, class, continue, def, del, elif, else,
# except, exec, finally, for, from, global, if, import, in, is, lambda, not, or, pass, print,
# raise, return, try, while, with and yield.
print("Reserved keywords example executed successfully.")

# You can get the list of all reserved keywords in Python using the keyword module
import keyword
print(keyword.kwlist)
print("Total number of keywords in Python:", len(keyword.kwlist))
# Output: List of all reserved keywords in Python
# Total number of keywords in Python: 35
