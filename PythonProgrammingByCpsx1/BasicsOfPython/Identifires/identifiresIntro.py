# Identifiers in Python
# What are Identifiers?
# Identifiers are names used to identify variables, functions, classes, modules, and other objects in Python.
# They are used to give a unique name to these objects so that they can be referenced and manipulated in the code.
# Rules for Naming Identifiers
# 1. An identifier can consist of letters (a-z, A-Z), digits (0-9), and underscores (_).
# 2. An identifier must start with a letter or an underscore (_). It cannot start with a digit.
# 3. Identifiers are case-sensitive, which means that 'myVar' and 'myvar' are considered different identifiers.
# 4. Identifiers cannot be the same as Python keywords (reserved words) such as 'if', 'else', 'while', etc.
# 5. There is no limit on the length of an identifier, but it is recommended to keep it concise and meaningful.
# Examples of Valid Identifiers
my_variable = 10
_myVar = 20
var123 = 30
print(my_variable , _myVar , var123)
# Examples of Invalid Identifiers
# 1var = 10      # Invalid: starts with a digit 
# my-var = 20    # Invalid: contains a hyphen (-)
# Keywords as Identifiers
# if = 10        # Invalid: 'if' is a reserved keyword in Python
print("Identifier naming rules examples executed successfully.")
# Best Practices for Naming Identifiers
# 1. Use meaningful names that describe the purpose of the variable or function.
# 2. Use lowercase letters and separate words with underscores for variable and function names (e.g., my_variable).
# 3. Use CamelCase for class names (e.g., MyClass).
# 4. Avoid using single-character names except for loop counters or temporary variables.
# 5. Follow a consistent naming convention throughout your codebase.    
print("Best practices for naming identifiers executed successfully.")
