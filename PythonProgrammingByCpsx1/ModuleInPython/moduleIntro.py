# Introduction to Modules in Python
# A module in Python is a file that contains Python code, which can define functions, classes, and variables.
# It allows for code organization and reuse across different programs.
# Creating a Module
# To create a module, simply save your Python code in a file with a .py extension.

# Types of Modules:
# 1. Built-in Modules: These are pre-installed with Python, such as math, sys, os, etc.
# 2. User-defined Modules: These are modules created by users to organize their code.
# 3. Third-party Modules: These are modules developed by the community and can be installed via package managers like pip.
# Importing Modules
# You can import a module using the import statement.

# 1. Built-in Modules: These are pre-installed with Python, such as math, sys, os, etc.
import math # Importing the built-in math module
# Using functions from the math module
print("Square root of 16 is:", math.sqrt(16))  # Output: 4.0
print("Value of Pi is:", math.pi)                # Output: 3.141592653589793
print()

